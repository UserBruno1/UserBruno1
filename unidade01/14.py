# 14. Elabore um programa que calcule e apresente 
# o volume de uma caixa retangular, por meio da 
#fórmula VOLUME = COMPRIMENTO * LARGURA * ALTURA.


comprimento = float(input("Digite o valor do comprimento da caixa: "))
largura = float(input("Digite o valor da largura da caixa: "))
altura = float(input("Digite o valor da altura da caixa: "))

volume = comprimento * largura * altura

print(f"O volume da caixa retangular é: {volume}")

